    private void btaAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btaAdicionarActionPerformed
  
 //preenche a tabela com ferramentas      
        FerramentaDAO dao = new FerramentaDAO();
        Ferramentas ferramentaSelecionada = (Ferramentas) comboBox.getSelectedItem();
        String produtorSEL = ferramentaSelecionada != null ? ferramentaSelecionada.getNome() : "";

        List<Ferramentas> ferra = dao.listar(produtorSEL);    


// Verifica se já existe na tabela
        boolean ferramentaExiste = false;
        for (int i = 0; i < tabelaFerramentaSEL.getRowCount(); i++) {
            String nomeExistente = tabelaFerramentaSEL.getValueAt(i, 0).toString(); // Obtém o nome da coluna 0
            if (nomeExistente.equals(produtorSEL)) {
                ferramentaExiste = true;
                break; // Sai do loop pois já encontrou
            }
        }

// Se não existe, adiciona
        if (!ferramentaExiste) {
            for (Ferramentas f : ferra) {
                if (f.getNome().equals(produtorSEL)) {
                    Object[] obj = {
                        f.getNome(), 
                        f.getPreco_diaria()
                    };
                    tabelaFerramentaSEL.addRow(obj);

                    // Adiciona o valor da ferramenta à soma total
                    double valorFerramenta = f.getPreco_diaria();                    
                    somaTotal += valorFerramenta; // Soma o valor da ferramenta
                }
            }
        }
        
        
        atualizarValorTotal(somaTotal);
        

    }//GEN-LAST:event_btaAdicionarActionPerformed

