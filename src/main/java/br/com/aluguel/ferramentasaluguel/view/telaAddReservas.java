    private void btaFinalizarCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btaFinalizarCompraActionPerformed
// Coleta os dados para a venda
        double valorTotal = somaTotal;
        String cpfCliente = txtCPF.getText();

// Validação do CPF
        if (!cpfCliente.matches("\\d{3}\\.\\d{3}\\.\\d{3}-\\d{2}")) {
            JOptionPane.showMessageDialog(null, "CPF inválido! Use o formato XXX.XXX.XXX-XX.");
            return;
        }

// Validação da data antes da conversão
        if (!dataLocacao.getText().matches("\\d{2}/\\d{2}/\\d{4}") || !dataPrevisao.getText().matches("\\d{2}/\\d{2}/\\d{4}")) {
            JOptionPane.showMessageDialog(null, "Data inválida! Use o formato DD/MM/AAAA.");
            return;
        }

// Conversão da string para LocalDate
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDate dateLocacao = LocalDate.parse(dataLocacao.getText(), formatter);
        LocalDate datePrevisao = LocalDate.parse(dataPrevisao.getText(), formatter);

FerramentaDAO ferramentasDAO = new FerramentaDAO(); // Criar um DAO para manipular ferramentas

List<Ferramentas> listFerramentas = new ArrayList<>();
for (int i = 0; i < tabelaFerramentaSEL.getRowCount(); i++) {
    String nomeFerramenta = tabelaFerramentaSEL.getValueAt(i, 0).toString();
    double precoFerramenta = Double.parseDouble(tabelaFerramentaSEL.getValueAt(i, 1).toString());

    // Busca no banco antes de criar uma nova
    Ferramentas ferramenta = ferramentasDAO.buscarPorNome(nomeFerramenta);
    if (ferramenta == null) {
        ferramenta = new Ferramentas();
        ferramenta.setNome(nomeFerramenta);
        ferramenta.setPreco_diaria(precoFerramenta);

        ferramenta = ferramentasDAO.salvar(ferramenta); // Persistir no banco
    }

    listFerramentas.add(ferramenta);
}


// Verifica se há ferramentas na lista
        if (listFerramentas.isEmpty()) {
            JOptionPane.showMessageDialog(null, "A lista de ferramentas não pode estar vazia!");
            return;
        }

// Busca o cliente pelo CPF
// Criando objeto Cliente e associando à reserva
        ClienteDAO dao = new ClienteDAO();
        Clientes clientes = new Clientes();    
        int RetornarID = dao.getIdClientePorCPF(cpfCliente);
        clientes.setId(RetornarID );
        

// Verifica se o cliente foi encontrado
        if (clientes == null) {
            JOptionPane.showMessageDialog(null, "Cliente não encontrado");
            return;
        }

// Criação do objeto de reserva e salvamento no banco
        Reservas reservas = new Reservas();
        reservas.setCliente(clientes);
        reservas.setData_locacao(dateLocacao);
        reservas.setData_prevista(datePrevisao);
        reservas.setFerramentas(listFerramentas);
        reservas.setPreco_total(valorTotal);

        ReservasDAO reservasdao = new ReservasDAO();
        reservasdao.cadastrarReserva(reservas);

// Limpa os campos da tela
        txtCPF.setText("");
        dataLocacao.setText("");
        dataPrevisao.setText("");
        comboBox.setSelectedIndex(-1);
        tabelaFerramentaSEL.setRowCount(0);

    }//GEN-LAST:event_btaFinalizarCompraActionPerformed

