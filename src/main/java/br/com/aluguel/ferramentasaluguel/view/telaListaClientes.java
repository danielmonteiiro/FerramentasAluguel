    /**
     * Função que preencher a tabela com as informações 
     */
    
  
    
 public void preencherTabela(List<Clientes> clientes) {
     String colunas[] = {"ID","Nome", "CPF", "Celular", "E-mail"};
     String dados[][] =new String[clientes.size()][colunas.length];
     
     int i = 0;
     
     for(Clientes c : clientes) {
         dados[i] = new String[] {
             String.valueOf(c.getId()),
             c.getNome(),
             c.getCpf(),
             String.valueOf(c.getCelular()),
             c.getEmail()
         };
         i++;
     }
    DefaultTableModel tabelaClientes = new DefaultTableModel(dados,colunas);
    tabela.setModel(tabelaClientes);
 }
    
 public void  atualizarTabelaClientes() {
    
    ClienteDAO dao = new ClienteDAO();
    List<Clientes> listar = dao.listar("");
     DefaultTableModel model = (DefaultTableModel) tabela.getModel();
    
    System.out.println("Total de clientes: " + listar.size()); // Depuração
    
    for (Clientes c : listar) {
        model.addRow(new Object[] {
            c.getId(),
            c.getNome(),
            c.getCpf(),
            c.getCelular(),
            c.getEmail()
        });
    }
}

    
    /**
     * Função que preencher a tabela com base no nome pesquisado
     */
    public void PreencherTabelaNome(String nome) {
        try {
            ClienteDAO dao = new ClienteDAO();
            List<Clientes> listar = dao.listar(nome);
            System.out.println("Total de clientes: " + listar.size()); // Adicione isso para depuração

            if (nome.equals("")) {
                JOptionPane.showMessageDialog(null, "Escreva um nome para pesquisar");
                return; // Retorna se a lista estiver vazia
            }
            if (listar == null || listar.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Nenhum cliente encontrado com o nome: " + nome);
                return; // Retorna se a lista estiver vazia
            }

            preencherTabela(listar);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao buscar clientes: " + e.getMessage());
        }
    }

    /*
    * Função para limpar a tabela quando algum objeto é editado ou excluido
    */
    public void limparTabela() {
        DefaultTableModel model = (DefaultTableModel) tabela.getModel();
        if (model.getRowCount() > 0) { // Evita erro se a tabela já estiver vazia
            model.setRowCount(0);
        } 
    }




