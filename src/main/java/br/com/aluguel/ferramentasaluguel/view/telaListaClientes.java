    private void btaExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btaExcluirActionPerformed
        int selectLinha = tabela.getSelectedRow();

// Verificar se alguma linha foi selecionada
        if (selectLinha != 1) {
            String idClientes = (String) tabela.getValueAt(selectLinha, 0);
            int id = Integer.parseInt(idClientes); // Converte String do id para INT

            ClienteDAO dao = new ClienteDAO();
            Clientes clientes = dao.buscarPorId(id);

            if (clientes != null) {
                // Exclui o cliente do banco de dados
                dao.excluirClientes(clientes);
                limparTabela();
                atualizarTabelaClientes();

                JOptionPane.showMessageDialog(this, "Cliente excluído com sucesso!");

            } else {
                JOptionPane.showMessageDialog(this, "Erro ao buscar o cliente.", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            // Mensagem de erro caso nenhuma linha esteja selecionada
            JOptionPane.showMessageDialog(this, "Nenhuma linha selecionada.", "Erro", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btaExcluirActionPerformed

    private void btaEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btaEditarActionPerformed
        int selectLinha = tabela.getSelectedRow();
        if (selectLinha != -1) {
            DefaultTableModel modeloTabela = (DefaultTableModel) tabela.getModel();

            String idClientes = (String) tabela.getValueAt(selectLinha, 0);
            int id = Integer.parseInt(idClientes); // Converte String do id para INT

            ClienteDAO dao = new ClienteDAO();
            Clientes clienteExiste = dao.buscarPorId(id);

            if (clienteExiste != null) {
                telaEditarCliente telaEditar = new telaEditarCliente(id);
                telaEditar.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao buscar o cliente.", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Nenhuma linha selecionada.", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btaEditarActionPerformed

